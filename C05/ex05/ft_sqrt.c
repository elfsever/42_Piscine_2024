int	ft_sqrt(int nb)
{
	long	i;

	if (nb == 0)
		return (0);
	i = 1;
	if (nb >= 1)
	{
		while (i * i <= nb)
		{
			if (i * i == nb)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
